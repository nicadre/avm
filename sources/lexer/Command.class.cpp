/* ************************************************************************** */
/*                                                                            */
/*                             Operator Overloads                             */
/*                                                                            */
/* ************************************************************************** */

Command					&	Command::operator=(Command const & rhs) {

	this->_arguments = rhs.arguments();
	this->_name = rhs.name();

	return *this;

}

/* ************************************************************************** */
/*                                                                            */
/*                                Destructors                                 */
/*                                                                            */
/* ************************************************************************** */

Command::~Command() {}

/* ************************************************************************** */
/*                                                                            */
/*                                  Setters                                   */
/*                                                                            */
/* ************************************************************************** */

Command					&	Command::arguments(bool const & arguments) {

	this->_arguments = arguments;

	return *this;

}

Command					&	Command::name(std::string const & name) {

	this->_name = name;

	return *this;

}

/* ************************************************************************** */
/*                                                                            */
/*                                  Getters                                   */
/*                                                                            */
/* ************************************************************************** */

bool			const	&	Command::arguments() const { return this->_arguments; }

std::string		const	&	Command::name() const { return this->_name; }

