Command::Command() {}

Command::Command(Command const & src) {

	*this = src;

}

Command::Command(std::string const & name, bool const & arguments) :
	_name(name), _arguments(arguments) {}

/* ************************************************************************** */
/*                                                                            */
/*                              Member Functions                              */
